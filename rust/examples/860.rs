fn sol(bills: Vec<i32>) -> bool {
    if bills[0] != 5 { return false }
    let (mut five, mut ten) = (1, 0);

    for bill in bills.iter().skip(1) {
        if 
            (bill == &10 && five == 0) ||
            (bill == &20 && (five < 3 && (five < 1 || ten < 1)))
        {
            return false;
        } 

        match bill {
            5 => five += 1,
            10 => {
                ten += 1;
                five -= 1;
            },
            20 => {
                if ten > 0 && five > 0 {
                    ten -= 1;
                    five -= 1;
                } else {
                    five -= 3;
                }
            },
            _ => ()
        }
        
    }

    true
}
